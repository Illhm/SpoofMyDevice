    private static DevicePreset createSamsungPreset(String modelName, String deviceCode, String board, String hardware, String release, int sdk) {
        DeviceProfile profile = new DeviceProfile();
        profile.setBrand("samsung");
        profile.setManufacturer("samsung");
        profile.setModel(deviceCode);
        profile.setProductName(board);
        profile.setDeviceCode(board);
        profile.setBoard(board);
        profile.setHardware(hardware);
        profile.setBoardPlatform(hardware);
        profile.setBuildRelease(release);
        profile.setBuildSdk(sdk);
        profile.applyFallbacks();
        return new DevicePreset("samsung_" + deviceCode.toLowerCase(), "Samsung", modelName, "Android " + release, profile);
    }
